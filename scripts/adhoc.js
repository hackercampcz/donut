function updateContact(key) {
  return dynamo.updateItem({
    TableName: "contacts",
    Key: key,
    UpdateExpression: "SET hasAllstarDiscount = :true",
    ExpressionAttributeValues: { ":true": true }
  });
}

async function* getAttendees(year) {
  const result = await dynamo.scan({
    TableName: "attendees",
    ProjectionExpression: "slackID, #name, company, events, image, ticketType",
    FilterExpression: "#y = :y",
    ExpressionAttributeNames: { "#y": "year", "#name": "name" },
    ExpressionAttributeValues: { ":y": year }
  });
  if (result.hasOwnProperty(Symbol.asyncIterator)) {
    for await (const page of result) {
      yield page.Items;
    }
  } else {
    yield result.Items;
  }
}

async function getAllAttendees() {
  const result = [];
  for await (const page of getAttendees(2024)) {
    result.push(...page);
  }
  return result;
}

function updateHackers() {
  return dynamo.updateItem({
    TableName: "attendees",
    ConditionExpression: "#year = :year AND ticketType = :hacker",
    UpdateExpression: "SET ticketPrice = :price",
    ExpressionAttributeNames: { "#year": "year" },
    ExpressionAttributeValues: { ":price": 6000, ":hacker": "hacker", ":year": 2024 }
  });
}

